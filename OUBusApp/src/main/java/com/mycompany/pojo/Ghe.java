public class Ghe {
    private int maGhe;
    private String tenGhe;

    public Ghe() {
    }

    public Ghe(int maGhe, String tenGhe) {
        this.maGhe = maGhe;
        this.tenGhe = tenGhe;
    }

    
    /**
     * @return the maGhe
     */
    public int getMaGhe() {
        return maGhe;
    }

    /**
     * @param maGhe the maGhe to set
     */
    public void setMaGhe(int maGhe) {
        this.maGhe = maGhe;
    }

    /**
     * @return the tenGhe
     */
    public String getTenGhe() {
        return tenGhe;
    }

    /**
     * @param tenGhe the tenGhe to set
     */
    public void setTenGhe(String tenGhe) {
        this.tenGhe = tenGhe;
    }
    
    
}
